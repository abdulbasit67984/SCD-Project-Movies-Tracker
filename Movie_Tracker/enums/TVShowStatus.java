package Movie_Tracker.enums;

/**
 * Enumeration for the possible statuses of a TV Show.
 * - WATCHING: The user is currently watching the TV show.
 * - COMPLETED: The user has finished watching all available episodes/seasons.
 * - TO_WATCH: The TV show is planned to be watched.
 */
public enum TVShowStatus {
    WATCHING,
    COMPLETED,
    TO_WATCH
}